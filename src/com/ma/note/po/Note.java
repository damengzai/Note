package com.ma.note.po;

public class Note {
	private int id;
	private int iu;
	private String noteContext;
	
	
	

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	
	public int getIu() {
		return iu;
	}
	public void setIu(int iu) {
		this.iu = iu;
	}
	public String getNoteContext() {
		return noteContext;
	}
	public void setNoteContext(String noteContext) {
		this.noteContext = noteContext;
	}
}
